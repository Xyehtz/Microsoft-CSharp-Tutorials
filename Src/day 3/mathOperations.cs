int sum = 7 + 5; // This will add two numbers
int difference = 7 - 5; // This will rest two numbers
int product = 7 * 5; // This will multiply two numbers
int quotient = 7 / 5; // This will divide two numbers

// Print the results
Console.WriteLine($"Sum: {sum}");
Console.WriteLine($"Difference: {difference}");
Console.WriteLine($"Product: {product}");
Console.WriteLine($"Quotient: {quotient}");

// To make operations with decimals, add a m at the end of the number

decimal decimalQuotient = 7.0m / 5;
Console.WriteLine($"Decimal quotient: {decimalQuotient}");