string firstName; // Create a new string
firstName = "Bob"; // Add a value to the string

int secondName; // Create a new integer
secondName = "Bob"; // Add a value to the integer, because it is a integer, the value must be a number, in this case there is an error
secondName = 184; // This would be a valid value to the secondName variable

Console.WriteLine(firstName); // Print the variable's value

// To re assign a value to a variable do the following

firstName = "Mary";
firstName = "Drake";
firstName = "Goofy ahh cat";

Console.WriteLine(firstName); // This will print the last value assign to the variable, in this case "Goofy ahh cat"

