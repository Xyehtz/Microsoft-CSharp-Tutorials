Console.WriteLine('b'); // This will print a "char" or character, characters can be printed using single quotes

Console.WriteLine("Hello World!"); // This will print a "string", strings must be printed using double quotes

Console.WriteLine(123); // This will print a "int" or integer, numbers need to be printed without using single nor double quotes

Console.WriteLine(0.25F); // This will print a "float", floats numbers need to have an F at the end to be printed

Console.WriteLine(2.625); // This will print a decimal number, when using double you don't need to add anything at the end of the number, this is the default value used by the compiler

Console.WriteLine(12.39816m); // M is used to tell the compiler that the value is a decimal number

Console.WriteLine(true); // This will print the boolean value True

Console.WriteLine(false); // This will print the boolean value False